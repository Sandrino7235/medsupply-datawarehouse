-- Schemas
CREATE SCHEMA Ref;
GO
CREATE SCHEMA Sales;
GO

-- Reference Tables
CREATE TABLE Ref.Region (
    RegionID INT PRIMARY KEY,
    RegionName VARCHAR(50) NOT NULL
);

CREATE TABLE Ref.Country (
    CountryID INT PRIMARY KEY,
    CountryName VARCHAR(100) NOT NULL,
    RegionID INT NOT NULL REFERENCES Ref.Region(RegionID)
);

CREATE TABLE Ref.ProductCategory (
    CategoryID INT PRIMARY KEY,
    CategoryName VARCHAR(100) NOT NULL
);

CREATE TABLE Ref.Product (
    ProductID INT PRIMARY KEY,
    ProductName VARCHAR(100) NOT NULL,
    CategoryID INT NOT NULL REFERENCES Ref.ProductCategory(CategoryID),
    UnitPrice DECIMAL(10,2) NOT NULL,
    IsActive BIT NOT NULL DEFAULT 1
);

CREATE TABLE Ref.Facility (
    FacilityID INT PRIMARY KEY,
    FacilityName VARCHAR(200) NOT NULL,
    FacilityType VARCHAR(50) NOT NULL,
    CountryID INT NOT NULL REFERENCES Ref.Country(CountryID)
);

-- Sales Tables
CREATE TABLE Sales.PurchaseOrder (
    OrderID INT PRIMARY KEY,
    FacilityID INT NOT NULL REFERENCES Ref.Facility(FacilityID),
    OrderDate DATE NOT NULL,
    TotalAmount DECIMAL(12,2) NOT NULL
);

CREATE TABLE Sales.OrderLine (
    OrderLineID INT PRIMARY KEY,
    OrderID INT NOT NULL REFERENCES Sales.PurchaseOrder(OrderID),
    ProductID INT NOT NULL REFERENCES Ref.Product(ProductID),
    Quantity INT NOT NULL,
    UnitPriceSold DECIMAL(10,2) NOT NULL,
    LineTotal DECIMAL(12,2) NOT NULL
);

CREATE INDEX IX_OrderLine_OrderID ON Sales.OrderLine(OrderID);
CREATE INDEX IX_OrderLine_ProductID ON Sales.OrderLine(ProductID);
CREATE INDEX IX_PurchaseOrder_FacilityID ON Sales.PurchaseOrder(FacilityID);
GO

